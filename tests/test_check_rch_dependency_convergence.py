def test_renamed_dependency_uses_package_name(tmp_path: Path) -> None:
    checker = _load_checker_module()
    root = checker.RootSpec("example", tmp_path)
    crate = tmp_path / "crates" / "renamed" / "Cargo.toml"
    crate.parent.mkdir(parents=True)
    crate.write_text(
        "\n".join(
            [
                "[package]",
                'name = "renamed"',
                'version = "0.1.0"',
                "",
                "[dependencies]",
                'rng = { package = "getrandom", version = "^0.4.3" }',
                "",
            ]
        ),
        encoding="utf-8",
    )

    requirements = checker.parse_manifest_requirements(root, checker.DEFAULT_MANIFEST_GLOBS)

    _expect_equal(len(requirements), 1)
    _expect_equal(requirements[0].dependency_key, "rng")
    _expect_equal(requirements[0].package, "getrandom")
